# 82. Write a Python program to calculate the sum of all items of a container (tuple, list, set, dictionary).


krotka = (1, 2, 3)
print ("Krotka: ", sum (krotka))


lista = [1, 2, 3]
print ("Lista: ", sum (lista))


zbiór = {1, 2, 3}
print ("Zbiór: ", sum (zbiór))


słownik = {'1': 1, '2': 2, '3': 3}
